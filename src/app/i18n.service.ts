@Injectable({ providedIn: 'root' })
export class I18nService {
  private readonly currentLanguage = signal<AppLanguage>('ru');

  readonly lang = this.currentLanguage.asReadonly();

  setLanguage(language: AppLanguage): void {
    this.currentLanguage.set(language);
  }

  t(key: string): string {
    const active = translations[this.currentLanguage()];
    const activeAdmin = adminTranslations[this.currentLanguage()];
    return active[key] ?? activeAdmin[key] ?? translations.ru[key] ?? adminTranslations.ru[key] ?? key;
  }

  localize(value: LocalizedText): string {
    return value[this.currentLanguage()] ?? value.ru ?? value.en;
  }
}
